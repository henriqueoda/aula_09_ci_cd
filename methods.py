def area_of_rectangle(width, height):
    area = width*height
    return area
 
def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def soma_calculadora(valor1, valor2):
    soma = valor1 + valor2
    return soma

def subtracao_calculadora(valor1, valor2):
    subtracao = valor1 - valor2
    return subtracao

